    <h3>Chính sách trả hàng</h3>
    <pre>
1. Điền thông tin trả hàng
Scan barcode bên dưới hoặc click vào link tại đây để điền thông tin trả hàng trong vòng 24h kể từ khi nhận hàng.  
2. Thời gian trả hàng:
Gửi ngay hàng đổi/ trả sau khi có SMS xác nhận trả hàng từ Cốm Food
Hàng đổi trả cần được chuyển phát nhanh hoặc giao trực tiếp đến Cốm Food trong 2 ngày với nội thành HN và tối đa 4 ngày với các vùng khác Sau khi khách nhận để Cốm Food có thể đăng bán tiếp cho khách khác. 
Nếu quá thời hạn trên mà Cốm Food chưa nhận được hàng trả, Cốm Food sẽ không nhận đổi trả món hàng mà sẽ hỗ trợ khách kí gửi món đồ đó.
Với đơn hàng chờ vận chuyển sau 3 ngày không áp dụng trả lại.
3. Tiền hoàn:
3.1 Với khách nhận tiền về tài khoản
Tiền sẽ được hoàn lại vào TK của người mua ngay Sau khi Cốm food nhận được hàng và sẽ được tính như sau:
Tiền nhận lại = Tiền hàng trên hóa đơn - tiền vận chuyển(nếu phải tính)
3.2. Với khách không lấy tiền về tài khoản và dùng tiền mua hàng tiếp theo.
Khách không nhận tiền về tài khoản sẽ không bị tính "phí trả hàng" ở công thức trên 4.1 và được quy đổi thành điểm vào tài khoản mua hàng. Khách hàng có thể tự mua và tiêu điểm bằng tài khoản của mình bất cứ khi nào.

