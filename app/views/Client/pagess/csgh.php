1. Chính sách giao hàng của các đơn hàng mua Online 
Nhằm mang đến sự lựa chọn phù hợp nhất cho khách hàng, Cóm Food triển khai 4 hình thức giao hàng Online:
Giao hàng hỏa tốc: Nhận hàng trong 30 phút - 2 tiếng
Giao hàng nhanh: Nhận hàng trong 4 tiếng - 6 tiếng
Giao hàng tiêu chuẩn: Nhận hàng trong 2-5 ngày
Chọn giờ giao hàng: Khách chọn giờ nhận hàng mong muốn
Tùy vào chính sách hỗ trợ riêng ở mỗi thời điểm, sản phẩm và địa điểm khách nhận hàng, đơn hàng sẽ thể hiện hình thức vận chuyển và mức phí cuối cùng khác nhau. 
Cốm food cấp đa dạng các dịch vụ giao hàng và thường xuyên cập nhật những ưu đãi phí giao hàng với mong muốn phục vụ khách hàng tốt nhất,
tuy nhiên thực tế vận hành có thể phát sinh những rủi ro ngoài ý muốn. Trong trường hợp này, quý khách có bất kỳ thông tin gì cần trao đổi vui lòng liên hệ bộ phận Chăm sóc khách hàng theo số Hotline miễn phí: 0363981299 (8h-22h, tất cả các ngày trong tuần).
Cốm Food cam kết sẽ kiểm tra đơn hàng và có chính sách giải quyết phù hợp với từng trường hợp cụ thể.
2. Quy trình giao nhận hàng
Khi nhận hàng, quý khách hàng cần lưu ý thực hiện:
- Mở gói hàng và đối chiếu hàng hóa với hoá đơn tính tiền
- Kiểm tra sản phẩm thực tế có đúng với sản phẩm mà Khách hàng đã đặt mua hay không.
- Kiểm tra bao bì và sản phẩm có bị hư hại do quá trình vận chuyển hay không.
Nếu không hài lòng với 1 trong 3 điều trên, Khách hàng có thể yêu cầu Nhân viên giao hàng xác nhận và trả lại hàng.
Cốm Food sẽ không chịu trách nhiệm giải quyết khiếu nại về việc thiếu hàng hoặc giao sai hàng của Khách hàng sau khi Khách hàng đã ký nhận và thanh toán.
Lưu ý: Đơn hàng sẽ tự động hủy nếu nhân viên giao hàng không liên lạc được với Khách hàng tại thời điểm giao hàng (tối đa 3 cuộc gọi và mỗi lần cách nhau 10 phút).


