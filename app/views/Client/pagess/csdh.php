    <h3>Chính sách đổi hàng</h3>
    <pre>
1. Những trường hợp được đổi trả:
Hàng bị hỏng do nhà sản xuất
Hàng bị hư hỏng do quá trình vận chuyển hàng cho khách của Cốm Food
Hàng giao không đúng mẫu mã, loại mà khách đã đặt
Hàng giao bị thiếu 
Nếu phát hiện những trường hợp trên, quý khách vui lòng không nhận hàng và yêu cầu nhân viên giao nhận của chúng tôi xác nhận tình trạng  ngay tại chỗ và yêu cầu đổi trả. Còn không khách hàng phải báo ngay tình trạng hàng bị hỏng về cho chúng tôi trong vòng 3 ngày kể từ ngày nhận hàng để được hỗ trợ đổi mới, quá 3 ngày chúng tôi sẽ không hỗ trợ giải quyết vì mặc định khách hàng đã đồng ý với sản phẩm được giao.

2. Điều kiện và quy định đổi trả chung:
“Một đổi một” đối với những sản phẩm bán ra trong vòng 7 ngày nếu có vấn đề trục trặc.
Sản phẩm bán ra sau 7 ngày: 
Còn đầy đủ bao bì, vỏ hộp, không bị trầy xước, móp méo, hỏng hóc bên ngoài, đổ vỡ..
Còn đầy đủ hóa đơn của cửa hàng
Thời gian đổi hàng không quá 30 ngày kể từ khi nhận hàng (căn cứ theo hóa đơn mua hàng hoặc biên lai ký nhận của bên giao hàng).
Tuy nhiên đối với một số sản phẩm chúng tôi sẽ không áp dụng những điều kiện đổi trả giống như trên, trong quá trình tư vấn bán hàng chúng tôi sẽ tư vấn cụ thể về thời hạn cũng như hình thức đổi trả cho từng sản phẩm để khách hàng tham khảo khi có nhu cầu mua sản phẩm đó.
